//: ## Episode 02: While Loops
// while loop -> check de condition before loop
var i = 1
while i < 10 {
    print("While loop \(i)")
    i += 1
}

print("Couting Again")

// repeat with while loop -> loop before check de condition
i = 1
repeat {
    print("Repeat with while loop \(i)")
    i += 1
} while i < 10
